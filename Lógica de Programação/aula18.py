# if / elif / else
# se/ se não se / se não

condicao1 = False
condicao2 = False
condicao3 = True
condicao4 = True

if condicao1:
    print('A condição 1 foi satisfeita')
elif condicao2:
    print('A condição 2 foi satisfeita')
elif condicao3:
    print('A condição 3 foi satisfeita')
elif condicao4:
    print('A condição 4 foi satisfeita')
else:
    print('Nenhuma condição foi satisfeita')

